//【例11】在数据库设计的四个阶段中，每个阶段都有自己的设计内容，为“哪些表，在哪些字段上，建什么样的索引”。这一设计应属于______设计阶段。
//分析：数据库设计方法中的新奥尔良方法，它将数据库设计过程分为四个阶段：需求分析、概念设计、逻辑设计、物理设计。其中：
//(1)需求分析的任务是在用户调查的基础上，通过分析，逐步明确用户对系统的需求，包括数据需求和围绕这些数据的业务处理需求。
//(2)概念设计的任务是产生反映企业组织信息需求的数据库概念结构，即概念模型。概念模型不依赖于计算机系统和具体的DBMS。设计概念模型的过程称为概念设计。
//(3)逻辑设计的任务是从概念模型导出特定的DBMS可以处理的数据库的逻辑结构(数据库的模式和外模式)，这些模式在功能、性能、完整性、一致性约束及数据库可扩充性等方面都满足用户的要求。
//(4)物理设计的任务是对于设计好的逻辑数据模型选择一个最符合要求的物理结构。物理设计的主要任务是对数据库中数据在物理设备上的存放结构和存取方法进行设计。
//答案：物理设计
//【例12】 数据流图表达了数据和处理的关系，数据字典则是系统中各类______的集合。
