//SELECT R.A, R.B, S.D, S.F FROM R, S WHERE____              __。
//分析：自然连接的定义是：设关系R和S有相同的属性名Ai(i = 1，…，k)，则R与S的自然连接是从R×S中选择满足条件R.A1 = S.A1∧R.A2 = S.A2∧…∧R.Ak = S.Ak的所有元组，再除掉各列后所组成的新关系，记为R∞S。而关系R和关系S的相同属性名是A。
