//数据字典通常包括数据项、数据结构、数据流、数据存储和处理过程五个部分。其中，数据项是数据的最小组成单位，若干个数据项可以组成一个数据结构，数据字典通过对数据项和数据结构的定义来描述数据流、数据存储的逻辑内容。
//答案：数据描述
//【例13】逻辑结构设计的任务就是把______阶段设计好的基本E - R图转换为与选用DBMS产品所支持的数据模型相符合逻辑结构。
//分析：概念结构是独立于任何一种数据模型的信息结构，逻辑结构设计的任务就是把概念结构设计阶段设计好的基本E - R图转换为与选用DBMS产品所支持的数据模型相符合逻辑结构。
//从理论上讲，设计逻辑结构应该选择最适于相应概念结构的数据模型，然后对所支持这种数据模型的各种DBMS进行比较，从中选出最合适的DBMS。但实际情况往往是给定了某种DBMS，设计人员没有选择的余地。目前，DBMS产品一般包括支持关系、网状、层次三种模型中的某一种，对某一种数据模型，各个机器系统又有不同的限制，提供不同的环境和工具。
//答案：概念结构设计
//【例14】数据转储是______中采用的基本技术。
