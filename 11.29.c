//分析：模式描述的是数据库数据的全局逻辑结构，外模式描述的是数据的局部逻辑结构。对应于同一个模式可以有任意多个外模式。对于每一个外模式，数据库系统都有一个外模式 / 模式映像，它定义该外模式与内模式之间的对应关系。这些映像定义通常包含在各自外模式的描述中。
//当模式改变时(例如增加新的关系、新的属性、改变属性的数据类型等)，则数据库管理员对各个外模式／模式的映像做相应改变，可以使外模式保持不变。应用程序是依据数据的外模式编写的，从而应用程序不必修改，保证了数据与程序的逻辑独立性，简称数据的逻辑独立性。
